    public GameMap(int tileSize) { //Set Tiles
        this.tileSize = tileSize;
        tiles = new Tile[DEFAULT_MAP.length][DEFAULT_MAP[0].length];

        for(int y = 0; y < getHeight(); y++) {
            for (int x = 0; x < getWidth(); x++) {
                tiles[y][x] = switch (DEFAULT_MAP[y][x]) { //Check Number -> Different Tile
                    case 1 -> new Block(x, y);
                    case 2 -> new Dot(x, y);
                    default -> new Air(x, y);
                };
            }
        }
    }

    public boolean isFree(int x, int y) { //Check if field is free
        if (x < 0 || y < 0 || x >= getWidth() || y >= getHeight()) {
            return false;
        }
        return !(tiles[y][x] instanceof Block);
    }

    //Getter-Setter

        public int getWidth () {
            return tiles[0].length;
        }
        public int getHeight () {
            return tiles.length;
        }
        public int getTileSize () {
            return tileSize;
        }
        public Tile getTile ( int x, int y){
            return tiles[y][x];
        }
        public void setTile ( int x, int y, Tile tile){
            tiles[y][x] = tile;
        }

    //Render

    @Override
    public void render(Graphics2D g, int tileSize) {
        for (Tile[] row: tiles) {
            for (Tile tile : row) {
                tile.render(g, tileSize);
            }
        }
    }
